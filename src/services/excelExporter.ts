        client.name,
        client.hours.toFixed(2),
        `$${client.revenue.toFixed(2)}`,
        client.hours > 0 ? `$${(client.revenue / client.hours).toFixed(2)}` : '$0.00',
        '1' // This would be calculated from actual project relationships
